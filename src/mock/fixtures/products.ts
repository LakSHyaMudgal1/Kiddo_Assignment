type BadgePos = 'top_left' | 'top_right' | 'bottom_left' | 'bottom_right';

const badge = (
  label: string,
  bg: string,
  pos: BadgePos = 'top_left',
) => ({
  label,
  position: pos,
  theme: {
    variant: 'filled' as const,
    background: bg as `#${string}`,
    textColor: '#FFFFFF' as `#${string}`,
    borderRadius: 4,
  },
});

